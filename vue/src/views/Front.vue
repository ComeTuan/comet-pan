<script>
import {setItemWithExpiry} from "@/App"
import {getItemWithExpiry} from "@/App"
import {updateItemWithExpiry} from "@/App"
export default {
  name: "FrontLayout",

  data() {
    return {
      top: '',
      notice: [],
      user: getItemWithExpiry("user"),

      totalSpace: 10 * 1024 * 1024 * 1024, // 10GB 总空间
      usedSpace: 0, // 从后端接口获取的已用空间（以字节为单位）
    }
  },

  //更新组件的状态和DOM以反映最新的空间使用情况
  mounted() {
    //获取用户已使用的空间大小
    this.getUsedSpace()
  },
  //组件的数据或计算属性发生变化后，组件重新渲染时被调用
  updated() {
    //重新获取用户已使用的空间大小
    this.getUsedSpace()
  },

  computed: {
    // 计算已用空间占比
    percentage() {
      return (this.usedSpace / this.totalSpace) * 100;
    },
    // 根据占比确定进度条颜色
    progressColor() {
      if (this.percentage >= 90) {
        return '#cb2b3d'; // 大于等于 90% 显示红色
      } else if (this.percentage >= 75) {
        return '#d76a24'; // 大于等于 75% 显示橙色
      } else if (this.percentage >= 50) {
        return '#ffd100'; // 大于等于 50% 显示黄色
      } else {
        return '#149958'; // 小于 50% 显示绿色
      }
    },
  },
  methods: {
    getUsedSpace() {
      this.$request.get('/files/space').then(res => {
        if (res.code === '200') {
          this.usedSpace = res.data
        } else {
          this.$message.error(res.code + ": " + res.msg)
        }
      })
    },
    updateUser() {
      this.user = getItemWithExpiry("user")   // 重新获取下用户的最新信息
    },
    // 退出登录
    logout() {
      this.$confirm('确定要退出吗？', '确认退出', {
        confirmButtonText: '退出',
        cancelButtonText: '取消',
        type: 'success',
        center: true
      }).then(response => {
        this.$request.delete("/logout").then(res => {
          if (res.code === '200') {
            this.$message.success("退出成功")
            localStorage.removeItem("user");
            this.$router.push("/login");
          }
          else {
            this.$message.error(res.code + ": " + res.msg)
          }
        }).catch(error => {
          this.$message.error("退出失败")
        });
      }).catch(error => {
        // 用户点击了取消按钮，可以在这里处理取消事件，比如关闭对话框
      });
    },
    goToUserProfile() {
      //路由到个人中心
      this.$router.push('/person');
    },
  }

}
</script>

